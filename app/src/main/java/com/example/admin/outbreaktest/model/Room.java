    public static boolean isOutbreak(Room[][] floor) {
        for (int i = 0; i < floor.length; i++) {
            for (int j = 0; j < floor[i].length; j++) {
                if( infectedNeighbor(floor,i,j)>4 )
                    return true;
            }
        }
        return false;
    }

    public static int infectedNeighbor(Room[][] floor,int x,int y){
        if( x >= 0  && x < floor.length && y >= 0 && y < floor[0].length){
            if (!floor[x][y].visited && floor[x][y].isInfected){
                floor[x][y].visited = true;
                return 1+infectedNeighbor(floor,x,y-1)
                        +infectedNeighbor(floor,x+1,y)
                        +infectedNeighbor(floor,x-1,y)
                        +infectedNeighbor(floor,x,y+1);
            }else{
                floor[x][y].visited = true;
                return 0;
            }
        }
        return 0;
    }

